
using apparelshop.Dtos;
using apparelshop.Models;

namespace apparelshop.Contracts
{
    public interface ICustomers
    {
        /// <summary>
        /// 查詢所有 Customers 資料
        /// </summary>
        /// <returns>所有顧客的資料集合</returns>
        Task<IEnumerable<Customers>> GetAllCustomers();

        /// <summary>
        /// 查詢單一 Customers 資料（依指定 id）
        /// </summary>
        /// <paramname="id">顧客的唯一識別碼</param>
        /// <returns>指定顧客的資料</returns>
        Task<Customers> GetCustomerById(int id);

        /// <summary>
        /// 新增 Customers 資料
        /// </summary>
        /// <paramname="customer">新增的顧客資料</param>
        /// <returns>新增顧客的資料</returns>
        //Task<CustomersForCreationDto> CreateCustomer(CustomersForCreationDto customer);
        Task<Customers> CreateCustomer(CustomersForCreationDto customer);

        /// <summary>
        /// 更新 Customers 資料（依指定 id）
        /// </summary>
        /// <paramname="id">顧客的唯一識別碼</param>
        /// <paramname="customer">更新的顧客資料</param>
        /// <returns>Task</returns>
        Task UpdateCustomer(int id, CustomersForUpdateDto customer);

        /// <summary>
        /// 刪除 Customers 資料（依指定 id）
        /// </summary>
        /// <paramname="id">顧客的唯一識別碼</param>
        /// <returns>Task</returns>
        Task DeleteCustomer(int id);
    }
}










































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































